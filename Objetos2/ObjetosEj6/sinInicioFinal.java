package ObjetosEj6;

public class sinInicioFinal {
	String cadena;
	
	
	
	public sinInicioFinal(String cadena) {
		this.cadena = cadena;
	}



	public String sininiciofinal() {
		int longitud = cadena.length();
		
		
		
		return cadena.substring(1,longitud-1);
		
		
		
		
	}

}
