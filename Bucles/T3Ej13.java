import java.util.Scanner;

public class T3Ej13 {
    public static void main(String[] args) {
        int n;
        System.out.println("Dime n");
        Scanner scanner = new Scanner(System.in);
        n = scanner.nextInt();
        for (int i = n; i > 0; i--) { 
            for (int j = 0; j < i; j++) { 
                System.out.print("* "); 
            
        }
        System.out.println();

       
    }
    }
}